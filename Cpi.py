def indices_tuple(CPI__db,n):
  assert n <= len(CPI__db.indices_s)
  assert n != 0
  
  indices = []
  i = 0
  for ind in CPI__db.indices_s:
    if (i == n):
      break
    if re.search(r'{}'.format(CPI__glob_index_stem),ind):
      indices.append(ind)
      i += 1
      
  assert i == n
  
  tup = '('
  N = len(indices)
  for i in range(N-1):
    tup += '{},'.format(indices[i])
  tup += '{})'.format(indices[N-1])
  
  return tup  
 
