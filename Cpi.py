    code +='{}\tif(sign == "-"):\n'.format(tab)
    code +='{}\t\tcomp{} = "0."\n'.format(tab,indx+1)
    code +='{}\telse:\n'.format(tab)
    code +='{}\t\tcomp{} = {}{}\n'.format(tab,indx+1,comp,append)
