# parsing system commands if any
def parse_sys_commands(arg):
  sys_commands = []
  
  for s in arg:
    if re.search(r"(?i)^Comm?and",s):
      s = re.sub(r'(?i)^Comm?and\["','',s)
      s = re.sub(r'"\]@$','',s)
      sys_commands.append(s)
      
  return sys_commands

# issue system commands on the output file:
