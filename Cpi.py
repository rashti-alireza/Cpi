    code +='{}\th            = arg[perm[1]]\n'.format(tab)
    code +='{}\targ[perm[1]] = arg[perm[0]]\n'.format(tab)
    code +='{}\targ[perm[0]] = h\n'.format(tab)
    code +='{}\tif(sign == "-"):\n'.format(tab)
    if (sign == "-"):
      comp2 = re.sub(r"^'","'-",comp)
    else:
      comp2 = comp
    code +='{}\t\tcomp{} = {}{}\n'.format(tab,indx+1,comp2,append)
